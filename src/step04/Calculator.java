package step04;

public class Calculator
{
    /** 프로덕션 코드 */
    int add(int i, int j)
    {
        return i + j;
    }

    int subtract(int i, int j)
    {
        return i - j;
    }

    int multiply(int i, int j)
    {
        return i * j;
    }

    int divide(int i, int j)
    {
        return i / j;
    }
}
